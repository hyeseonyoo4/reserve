package com.example.reserve.types;

public enum CompareType {
    // // 예: "==", "!=", ">", "<", ">=", "<="
    EQUALS,       // ==
    NOT_EQUALS,   // !=
    GREATER_THAN, // >
    LESS_THAN,    // <
    GREATER_THAN_OR_EQUAL, // >=
    LESS_THAN_OR_EQUAL,    // <=
//    CONTAINS,     // 문자열 포함 여부
//    NOT_CONTAINS, // 문자열 미포함 여부
//    STARTS_WITH,  // 문자열 시작 여부
//    ENDS_WITH,    // 문자열 끝 여부
//    IN,           // 값이 목록에 포함 여부
//    NOT_IN,       // 값이 목록에 미포함 여부
//    IS_NULL,      // 값이 null 여부
//    IS_NOT_NULL,  // 값이 null이 아님 여부
//    MATCHES,      // 정규 표현식과 일치 여부
//    NOT_MATCHES,  // 정규 표현식과 일치하지 않음 여부
//    BETWEEN,      // 값이 범위 내에 있는지 여부
}
